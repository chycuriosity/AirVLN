if command -v conda >/dev/null 2>&1; then
  eval "$(conda shell.bash hook)"
  conda activate cxj
fi

cd ./AirVLN
echo $PWD

export PYTHONPATH="$PWD${PYTHONPATH:+:$PYTHONPATH}"

CLOUD_CONFIG=${LOCAL_EVAL_CLOUD_CONFIG:-}
CKPT_PATH=${LOCAL_EVAL_CKPT_PATH:-/data/lyj/cxj/AirVLN_ws/DATA/output/AirVLN-seq2seq/train/checkpoint/20251123-101135-680949/ckpt.LAST.pth}
RUN_NAME=${LOCAL_EVAL_NAME:-AirVLN-seq2seq}
EVAL_DATASET=${LOCAL_EVAL_DATASET:-val_unseen}
EVAL_NUM=${LOCAL_EVAL_NUM:--1}
MAX_ACTION=${LOCAL_EVAL_MAX_ACTION:-500}
BATCH_SIZE=${LOCAL_EVAL_BATCH_SIZE:-1}
CUDA_VISIBLE_DEVICES=${LOCAL_EVAL_CUDA_VISIBLE_DEVICES:-2}
GPU_DEVICE=${LOCAL_EVAL_GPU_DEVICE:-0}
SIMULATOR_TOOL_PORT=${LOCAL_SIMULATOR_TOOL_PORT:-30000}
LOCAL_RESUME=${LOCAL_EVAL_RESUME:-false}
LOCAL_RESUME_TIME=${LOCAL_EVAL_RESUME_TIME:-}
LOCAL_EPISODE_LIST_PATH=${LOCAL_EVAL_EPISODE_LIST_PATH:-}
LOCAL_SAVE_EPISODE_LIST=${LOCAL_EVAL_SAVE_EPISODE_LIST:-false}
INTERSECTION_MODE=${LOCAL_INTERSECTION_EVAL_MODE:-off}
INTERSECTION_WRONG_POLICY=${LOCAL_INTERSECTION_WRONG_POLICY:-branch_mismatch}
INTERSECTION_CANDIDATE_MODE=${LOCAL_INTERSECTION_CANDIDATE_MODE:-cheap}
INTERSECTION_TURN_WINDOW=${LOCAL_INTERSECTION_TURN_WINDOW:-4}
INTERSECTION_MAX_EVENTS_PER_EPISODE=${LOCAL_INTERSECTION_MAX_EVENTS_PER_EPISODE:--1}
INTERSECTION_COOLDOWN_STEPS=${LOCAL_INTERSECTION_COOLDOWN_STEPS:-0}
INTERSECTION_MAX_CORRECTIONS_PER_EPISODE=${LOCAL_INTERSECTION_MAX_CORRECTIONS_PER_EPISODE:--1}
INTERSECTION_MAX_CORRECTIONS_PER_CLUSTER=${LOCAL_INTERSECTION_MAX_CORRECTIONS_PER_CLUSTER:--1}
INTERSECTION_DETECTOR=${LOCAL_INTERSECTION_DETECTOR:-cloud}
INTERSECTION_CLOUD_CONFIDENCE_THRESHOLD=${LOCAL_INTERSECTION_CLOUD_CONFIDENCE_THRESHOLD:-0.5}
INTERSECTION_SAVE_INPUTS=${LOCAL_INTERSECTION_SAVE_INPUTS:-false}

export CUDA_VISIBLE_DEVICES

CMD=(python -u ./src/vlnce_src/train.py)
if [ -n "$CLOUD_CONFIG" ]; then
  CMD+=(--cloud_config "$CLOUD_CONFIG")
fi
if [ "$LOCAL_RESUME" = "true" ] || [ "$LOCAL_RESUME" = "1" ]; then
  CMD+=(--local_eval_resume)
fi
if [ -n "$LOCAL_RESUME_TIME" ]; then
  CMD+=(--local_eval_resume_time "$LOCAL_RESUME_TIME")
fi
if [ -n "$LOCAL_EPISODE_LIST_PATH" ]; then
  CMD+=(--local_eval_episode_list_path "$LOCAL_EPISODE_LIST_PATH")
fi
if [ "$LOCAL_SAVE_EPISODE_LIST" = "true" ] || [ "$LOCAL_SAVE_EPISODE_LIST" = "1" ]; then
  CMD+=(--local_eval_save_episode_list)
fi
if [ "$INTERSECTION_SAVE_INPUTS" = "true" ] || [ "$INTERSECTION_SAVE_INPUTS" = "1" ]; then
  CMD+=(--intersection_save_inputs)
fi

"${CMD[@]}" \
--run_type eval \
--policy_type seq2seq \
--collect_type TF \
--name "$RUN_NAME" \
--batchSize "$BATCH_SIZE" \
--trainer_gpu_device "$GPU_DEVICE" \
--EVAL_CKPT_PATH_DIR "$CKPT_PATH" \
--EVAL_DATASET "$EVAL_DATASET" \
--EVAL_NUM "$EVAL_NUM" \
--maxAction "$MAX_ACTION" \
--simulator_tool_port "$SIMULATOR_TOOL_PORT" \
--intersection_eval_mode "$INTERSECTION_MODE" \
--intersection_wrong_policy "$INTERSECTION_WRONG_POLICY" \
--intersection_candidate_mode "$INTERSECTION_CANDIDATE_MODE" \
--intersection_turn_window "$INTERSECTION_TURN_WINDOW" \
--intersection_max_events_per_episode "$INTERSECTION_MAX_EVENTS_PER_EPISODE" \
--intersection_correction_cooldown_steps "$INTERSECTION_COOLDOWN_STEPS" \
--intersection_max_corrections_per_episode "$INTERSECTION_MAX_CORRECTIONS_PER_EPISODE" \
--intersection_max_corrections_per_cluster "$INTERSECTION_MAX_CORRECTIONS_PER_CLUSTER" \
--intersection_detector "$INTERSECTION_DETECTOR" \
--intersection_cloud_confidence_threshold "$INTERSECTION_CLOUD_CONFIDENCE_THRESHOLD"
